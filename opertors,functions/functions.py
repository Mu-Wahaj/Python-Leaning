import math
# Rounding numbers 

x=4.933333
round(x) # 5
round(x,1) # 4.9
round(x,2) # 4.93
round(x,3) # 4.933
print(round(x,4)) # 4.9333

# Absolute value will return the positive value of a number
y=-5
print(abs(y)) # 5

print(math.ceil(x)) # 5
print(math.floor(x)) # 4

# modulus operator returns the remainder of a division operation
print(10 % 3) # 1
math.sqrt(16) # 4.0
math.pow(2,3) # 8.0
math.sin(math.pi/2) # 1.0
math.cos(0) # 1.0
math.tan(math.pi/4) # 1.0
math.log(100,10) # 2.0
math.exp(1) # 2.718281828459045
math.factorial(5) # 120


